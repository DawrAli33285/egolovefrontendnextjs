  type PillarData = Record<number, { ego: number; love: number; count: number }>
  type PillarPercents = Record<number, { ego: number; love: number }>
  
  export function computeFreeScores(answers: Record<number, number>): PillarData {
    const pillarData: PillarData = {};
    for (let i = 0; i < FREE_QUESTIONS.length; i++) {
      const q = FREE_QUESTIONS[i];
      const chosen = answers[i];
      if (chosen === undefined) continue;
      const score = q.scores[chosen];
      if (!pillarData[q.pillar]) pillarData[q.pillar] = { ego: 0, love: 0, count: 0 };
      pillarData[q.pillar].ego   += score.ego;
      pillarData[q.pillar].love  += score.love;
      pillarData[q.pillar].count += 1;
    }
    return pillarData;
  }
  
  export function pillarPercents(pillarData: PillarData): PillarPercents {
    const result: PillarPercents = {};
    for (const [pid, d] of Object.entries(pillarData)) {
      const max = d.count * 10;
      result[Number(pid)] = {
        ego:  max > 0 ? Math.round((d.ego  / max) * 100) : 50,
        love: max > 0 ? Math.round((d.love / max) * 100) : 50,
      };
    }
    return result;
  }
  
  export function globalPercents(pillarPercentMap: PillarPercents): { ego: number; love: number } {
    let weightedEgo = 0, weightedLove = 0, totalWeight = 0;
    for (const p of PILLARS) {
      const pp = pillarPercentMap[p.id];
      if (!pp) continue;
      weightedEgo   += pp.ego  * p.weight;
      weightedLove  += pp.love * p.weight;
      totalWeight   += p.weight;
    }
    if (totalWeight === 0) return { ego: 50, love: 50 };
    return {
      ego:  Math.round(weightedEgo  / totalWeight),
      love: Math.round(weightedLove / totalWeight),
    };
  }
  
